### dias da semna 1 a 7 ########
dia = int(input("digite um numero de 1 a 7 para vermos q dia é hoje 1(domingo) 7(sabado): "))
if (dia==1):
    print("HOJE É DOMINGO")
elif(dia==2):
    print("HOJE É SEGUNDA")
elif(dia==3):
    print("HOJE É TERÇA")
elif(dia==4):
    print("HOJE É QUARTA")
elif(dia==5):
    print("HOJE É QUINTA")
elif(dia==6):
    print("HOJE É SEXTA")
elif(dia==7):
    print("HOJE É SABADO")
else:
    print("NUMERO INVALIDO")